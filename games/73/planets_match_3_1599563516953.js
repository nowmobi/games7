		function getTargests(argNum) {
			var arr = [];
			var tot = 0;
			for (var i = 0; i < 4; i++) {
				var min = (argNum / 8);
				var add = (argNum / 4);
				var num = Math.round(min + add * Math.random());
				arr.push(num);
				tot += num;
			}
			if (tot < argNum) {
				sortNumArray(arr);
				while (tot < argNum) {
					for (var i = 0; i < 4 && tot < argNum; i++) {
						arr[i]++;
						tot++;
					}
				}
			} else {
				sortNumArray(arr, true);
				while (tot > argNum) {
					for (var i = 0; i < 4 && tot > argNum; i++) {
						arr[i]--;
						tot--;
		
					}
				}
			}
			arr.push(num);
			return arr;
		}
		
