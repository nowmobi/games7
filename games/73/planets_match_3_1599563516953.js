		function getAllMatches() {
			var matches = [];
			var sItem;
			var tmparr;
			var blk;
			for (blk of blockArray) {
				sItem = blk;
				tmparr = [sItem];
				while (sItem.right != null && sItem.item.currentFrame == sItem.right.item.currentFrame) {
					tmparr.push(sItem.right);
					sItem = sItem.right;
				}
				if (tmparr.length >= 3) {
					for (var mat of tmparr) {
						if (matches.indexOf(mat) < 0) {
							matches.push(mat);
						}
					}
				}
				sItem = blk;
				tmparr = [sItem];
				while (sItem.down != null && sItem.item.currentFrame == sItem.down.item.currentFrame) {
					tmparr.push(sItem.down);
					sItem = sItem.down;
				}
				if (tmparr.length >= 3) {
					for (mat of tmparr) {
						if (matches.indexOf(mat) < 0) {
							matches.push(mat);
						}
					}
				}
			}
			return matches;
		}
		
